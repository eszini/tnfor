               ASSET_ALLOCATOR = FLOAT(ASSET_CLASS_VECTOR)
            ELSEIF(ASSET_CLASS_VECTOR > 0) THEN
               ASSET_ALLOCATOR = -FLOAT(ASSET_CLASS_VECTOR)
            ELSE
