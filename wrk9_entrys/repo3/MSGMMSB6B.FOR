C***********************************************************************
      SUBROUTINE LAG_CASH_USING_PATTERN_ALL_YRS(R_YRS,
     +                                          R_BOOK_VALUES,
     +                                          R_CASH_LAG_PATTERN,
     +                                          R_CASH_PAYMENTS,
     +                                          R_CASH_VALUES)
C***********************************************************************
C
      INTEGER*2 R_YRS
      REAL*4 R_BOOK_VALUES(0:12,0:R_YRS),
     +       R_CASH_LAG_PATTERN(12),
     +       R_CASH_PAYMENTS(12),
     +       R_CASH_VALUES(0:12,0:R_YRS)
      INTEGER*2 MO,YR,LAG_MO
C
C
         DO YR = 1, R_YRS
            DO MO = 1, 12
C MOVE CASH ACCURRED LAST YEAR TO THIS YEAR
               R_CASH_VALUES(MO,YR) = R_CASH_PAYMENTS(MO)
               R_CASH_PAYMENTS(MO) = 0.
            ENDDO
            DO MO = 1, 12
               DO LAG_MO = 1, 13-MO
                  R_CASH_VALUES(MO+LAG_MO-1,YR) =
     +               R_CASH_VALUES(MO+LAG_MO-1,YR) +
     +                 R_CASH_LAG_PATTERN(LAG_MO) * R_BOOK_VALUES(MO,YR)

               ENDDO
               DO LAG_MO = 14-MO, 12
                  R_CASH_PAYMENTS(LAG_MO+MO-13) =
     +                 R_CASH_LAG_PATTERN(LAG_MO) * R_BOOK_VALUES(MO,YR)
               ENDDO
               R_CASH_VALUES(0,YR) = R_CASH_VALUES(0,YR) +
     +                               R_CASH_VALUES(MO,YR)
            ENDDO
         ENDDO
      RETURN
      END
