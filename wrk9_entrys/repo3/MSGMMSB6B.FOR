C***********************************************************************
      FUNCTION LOAD_MASTER_CLASS_LIST(MAX_CLASS_NUM,TEMP_LIST,
     +                                                MASTER_CLASS_LIST)
C***********************************************************************
C
      INTEGER*2 LOAD_MASTER_CLASS_LIST,I
      INTEGER*2 MAX_CLASS_NUM,TEMP_LIST(MAX_CLASS_NUM),
     +          MASTER_CLASS_LIST(0:MAX_CLASS_NUM)
C
         LOAD_MASTER_CLASS_LIST = 0
