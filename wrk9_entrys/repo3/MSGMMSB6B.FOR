C***********************************************************************
      FUNCTION ASSET_CLASS_LINKAGES(asset_cls_loc,R_ASSET_CLASS_LIST,
     +                              ASSET_CLASS_VECTOR,
     +                              R_ASSET_ALLOCATION_LIST)
C***********************************************************************
C
       use SpinDriftLib
      use prod_arrays_dimensions
      USE PROD_ARRAYS_DIMENSIONS
      use sizecom
      SAVE

      INTEGER*2 CLASS_POINTER,VECTOR_POINTER,asset_cls_loc,LINKED_CLASS,
     +          ASSET_CLASS_VECTOR,I
      REAL*4 VECTOR_ASSET_CLASSES(AVAIL_DATA_YEARS),
     +       VECTOR_CLASS_ALLOCATIONS(AVAIL_DATA_YEARS),
     +       ASSET_CLASS_LIST(1024),
     +       ASSET_ALLOCATION_LIST(1024),
     +       R_ASSET_CLASS_LIST(*),
     +       R_ASSET_ALLOCATION_LIST(*)
      REAL*4 ASSET_ALLOCATOR
      CHARACTER*1 DUMMY_TYPE
      LOGICAL*1 ASSET_CLASS_LINKAGES,RETURN_ASSET_CLASS_LISTS
C
         ASSET_CLASS_LIST = -99.
         ASSET_ALLOCATION_LIST = 0.
         CLASS_POINTER = 1
         IF(asset_cls_loc < 0) THEN
            CALL GET_ASSET_VAR(ABS(asset_cls_loc),DUMMY_TYPE,
     +                                             VECTOR_ASSET_CLASSES)
            CALL GET_ASSET_VAR(ABS(ASSET_CLASS_VECTOR),DUMMY_TYPE ,
     +                                         VECTOR_CLASS_ALLOCATIONS)
            VECTOR_POINTER = 1
            DO
               asset_cls_loc = VECTOR_ASSET_CLASSES(VECTOR_POINTER)
               ASSET_CLASS_LIST(CLASS_POINTER) = asset_cls_loc
               ASSET_ALLOCATOR=VECTOR_CLASS_ALLOCATIONS(VECTOR_POINTER)
               ASSET_ALLOCATION_LIST(CLASS_POINTER) = ASSET_ALLOCATOR
               DO
                  CLASS_POINTER = CLASS_POINTER + 1
                  CALL GET_DEBT_LINKED_CLASS(asset_cls_loc,LINKED_CLASS)
                  IF(LINKED_CLASS < 0 .OR. CLASS_POINTER > 1000) EXIT
                  ASSET_CLASS_LIST(CLASS_POINTER) = LINKED_CLASS
                  ASSET_ALLOCATION_LIST(CLASS_POINTER) = ASSET_ALLOCATOR
                  asset_cls_loc = LINKED_CLASS
               ENDDO
               VECTOR_POINTER = VECTOR_POINTER + 1
               IF(VECTOR_POINTER > AVAIL_DATA_YEARS) EXIT
               IF(VECTOR_ASSET_CLASSES(VECTOR_POINTER) == 0 .OR.
     +                                       CLASS_POINTER >= 1000) EXIT
            ENDDO
            ASSET_CLASS_LINKAGES = CLASS_POINTER > VECTOR_POINTER
         ELSE
            ASSET_CLASS_LIST(CLASS_POINTER) = asset_cls_loc
            IF(ASSET_CLASS_VECTOR /= 0) THEN
               ! POINTS TO A TIME VECTOR
               ASSET_ALLOCATOR = -ABS(ASSET_CLASS_VECTOR)
            ELSE
               ASSET_ALLOCATOR = 100.
            ENDIF
            ASSET_ALLOCATION_LIST(CLASS_POINTER) = ASSET_ALLOCATOR
            DO
               CLASS_POINTER = CLASS_POINTER + 1
               CALL GET_DEBT_LINKED_CLASS(asset_cls_loc,LINKED_CLASS)
               IF(LINKED_CLASS < 0 .OR. CLASS_POINTER >= 1000) then
                EXIT
               endif
               ASSET_CLASS_LIST(CLASS_POINTER) = LINKED_CLASS
               ASSET_ALLOCATION_LIST(CLASS_POINTER) = ASSET_ALLOCATOR
               asset_cls_loc = LINKED_CLASS
            ENDDO
            ASSET_CLASS_LINKAGES = CLASS_POINTER > 2
         ENDIF
         IF(CLASS_POINTER <= AVAIL_DATA_YEARS) THEN
            ASSET_CLASS_LIST(CLASS_POINTER) = -99.
            ASSET_ALLOCATION_LIST(CLASS_POINTER) = 0.
         ENDIF
         DO I = 1, CLASS_POINTER
            R_ASSET_CLASS_LIST(I) = ASSET_CLASS_LIST(I)
            R_ASSET_ALLOCATION_LIST(I) = ASSET_ALLOCATION_LIST(I)
         ENDDO
      RETURN
C***********************************************************************
      ENTRY RETURN_ASSET_CLASS_LISTS(asset_cls_loc,
     +                               R_ASSET_CLASS_LIST,
     +                               ASSET_CLASS_VECTOR,
     +                               R_ASSET_ALLOCATION_LIST)
C***********************************************************************
C
         VECTOR_ASSET_CLASSES = -99.
         VECTOR_CLASS_ALLOCATIONS = 0.
         CLASS_POINTER = 1
         IF(asset_cls_loc < 0) THEN
            CALL GET_ASSET_VAR(ABS(asset_cls_loc),DUMMY_TYPE,
     +                                             VECTOR_ASSET_CLASSES)
            CALL GET_ASSET_VAR(ABS(ASSET_CLASS_VECTOR),DUMMY_TYPE ,
     +                                         VECTOR_CLASS_ALLOCATIONS)
            DO I = 1, AVAIL_DATA_YEARS
               R_ASSET_CLASS_LIST(I) = VECTOR_ASSET_CLASSES(I)
               R_ASSET_ALLOCATION_LIST(I) = VECTOR_CLASS_ALLOCATIONS(I)
            ENDDO
            RETURN_ASSET_CLASS_LISTS = .TRUE.
         ELSE
            R_ASSET_CLASS_LIST(1) = asset_cls_loc
            R_ASSET_CLASS_LIST(2) = -99
